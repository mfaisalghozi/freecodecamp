/**
 * JS boolean is a separate data type which can have two values only
 * that are true and false.
 * 
 * Note: if we quote with these values then they are just the normal string
 */

var a = true;
var b = false;
var c = "true";             // don't use these boolean values like this
var d = "true";

console.log(typeof a);      // boolean  <<--- output
console.log(typeof b);      // boolean  <<--- output
console.log(typeof c);      // string   <<--- output
console.log(typeof d);      // string   <<--- output

